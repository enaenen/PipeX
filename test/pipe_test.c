#include <stdio.h>
#include <unistd.h>

int	main(void)
{
	int	fd1[2];
	int	fd2[2];
	int	fd3[2];

	pipe(fd1);
	pipe(fd2);
	pipe(fd3);

	printf("fd1[0] == %d , fd1[1] == %d \n", fd1[0], fd1[1]);
	printf("fd2[0] == %d , fd2[1] == %d \n", fd2[0], fd2[1]);
	printf("fd3[0] == %d , fd3[1] == %d \n", fd3[0], fd3[1]);

}
